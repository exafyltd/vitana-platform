#!/usr/bin/env node
/**
 * generate-route-manifest.mjs — extract the gateway's mounted route prefixes
 * from services/gateway/src/index.ts into route-manifest.json.
 *
 * The manifest drives the GCP↔AWS staging parity probe: for every mounted
 * prefix we can ask both environments "is this route mounted?" using the
 * JSON-vs-HTML 404 diagnostic (CLAUDE.md §15) — an Express default 404 is
 * text/html, while any mounted router answers JSON (200/400/401/403/404).
 *
 * Re-run this script whenever gateway routes change, and commit the
 * regenerated manifest:
 *   node scripts/aws-staging-validation/generate-route-manifest.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..', '..');
const indexTs = readFileSync(
  join(repoRoot, 'services', 'gateway', 'src', 'index.ts'),
  'utf8',
);

const prefixes = new Set();

// mountRouterSync(app, '/api/v1/xyz', …) — the governed mount path.
for (const m of indexTs.matchAll(/mountRouterSync\(\s*app,\s*'([^']+)'/g)) {
  prefixes.add(m[1]);
}
// Plain app.use('/prefix', …) mounts (stripe webhooks, static assets).
for (const m of indexTs.matchAll(/app\.use\(\s*'([^']+)'/g)) {
  prefixes.add(m[1]);
}

// Root-level mounts ('/' and '/api/v1') probe everything and nothing —
// replace them with the always-on diagnostic endpoints instead.
prefixes.delete('/');
prefixes.delete('/api/v1');

// Route params → deterministic probe values. Any syntactically valid value
// works: we only assert the router is MOUNTED (JSON answer), not that the
// entity exists.
const PARAM_VALUE = '00000000-0000-0000-0000-000000000000';
const probePath = (p) => p.replace(/:[A-Za-z_]+/g, PARAM_VALUE);

const routes = [...prefixes].sort().map((prefix) => ({
  prefix,
  probe_path: probePath(prefix),
}));

const manifest = {
  _comment:
    'Generated by generate-route-manifest.mjs from services/gateway/src/index.ts — do not edit by hand.',
  source: 'services/gateway/src/index.ts',
  route_count: routes.length,
  routes,
};

const out = join(here, 'route-manifest.json');
writeFileSync(out, JSON.stringify(manifest, null, 2) + '\n');
console.log(`Wrote ${routes.length} route prefixes to ${out}`);
