#!/usr/bin/env node
/**
 * VTID-03277 — Generate the Guided Journey checklist seed SQL from the
 * curriculum v2 doc's "250 Topic Card Catalog" table.
 *
 * Deterministic: parses the markdown table (session | story purpose | cards),
 * derives chapter + inferred business gate, and emits an idempotent seed
 * migration. Re-running UPDATEs only the STRUCTURAL fields (label, session,
 * position, chapter, short_description, business_gate) on conflict — it never
 * clobbers authored content (voice script, explanation, practice target), so
 * admin edits survive a re-seed.
 *
 *   node scripts/journey/generate-checklist-seed.mjs \
 *     > supabase/migrations/<ts>_VTID_03277_journey_checklist_seed.sql
 */
import { readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DOC = resolve(
  __dirname,
  '../../docs/superpowers/specs/2026-06-04-maxina-90-day-journey-curriculum-v2.md',
);

const sq = (s) => (s == null ? null : String(s).replace(/'/g, "''"));
const lit = (s) => (s == null ? 'NULL' : `'${sq(s)}'`);

/** 6 chapters of 15 sessions (design-spec catalog structure). */
function chapterFor(session) {
  if (session <= 15) return 'basics';
  if (session <= 30) return 'daily_use';
  if (session <= 45) return 'community';
  if (session <= 60) return 'health';
  if (session <= 75) return 'intelligence';
  return 'discovery';
}

/** Inferred business gate (admins refine). Business doorway opens at S60. */
function businessGateFor(session) {
  if (session >= 76) return 'builder';
  if (session >= 70) return 'active';
  if (session >= 60) return 'curious';
  return null;
}

function parseCatalog(md) {
  const lines = md.split(/\r?\n/);
  const rows = [];
  const rowRe = /^\|\s*(\d{1,2})\s*\|\s*(.*?)\s*\|\s*(.*?)\s*\|\s*$/;
  const cardRe = /^(T\d{3})\s+(.+)$/;
  for (const line of lines) {
    const m = line.match(rowRe);
    if (!m) continue;
    const session = Number(m[1]);
    if (!Number.isInteger(session) || session < 1 || session > 90) continue;
    const purpose = m[2].trim();
    const cards = m[3]
      .split(';')
      .map((c) => c.trim())
      .filter(Boolean);
    const parsedCards = [];
    for (const c of cards) {
      const cm = c.match(cardRe);
      if (!cm) continue;
      parsedCards.push({ topicId: cm[1], label: cm[2].trim() });
    }
    if (parsedCards.length) rows.push({ session, purpose, cards: parsedCards });
  }
  return rows;
}

function main() {
  const md = readFileSync(DOC, 'utf8');
  const rows = parseCatalog(md);

  // Validate parse against the curriculum rules before emitting.
  const sessions = new Set(rows.map((r) => r.session));
  let topicCount = 0;
  const errors = [];
  for (const r of rows) {
    topicCount += r.cards.length;
    const expected = r.session <= 20 ? 2 : 3;
    if (r.cards.length !== expected) {
      errors.push(`session ${r.session}: expected ${expected} cards, got ${r.cards.length}`);
    }
  }
  if (sessions.size !== 90) errors.push(`expected 90 sessions, parsed ${sessions.size}`);
  if (topicCount !== 250) errors.push(`expected 250 topics, parsed ${topicCount}`);
  const ids = rows.flatMap((r) => r.cards.map((c) => c.topicId));
  if (new Set(ids).size !== ids.length) errors.push('duplicate topic ids parsed');
  if (errors.length) {
    console.error('SEED PARSE FAILED:\n' + errors.join('\n'));
    process.exit(1);
  }

  const out = [];
  out.push('-- =============================================================================');
  out.push('-- VTID-03277 — Guided Journey checklist seed (250 topics, status=draft)');
  out.push('-- GENERATED by scripts/journey/generate-checklist-seed.mjs from');
  out.push('-- docs/superpowers/specs/2026-06-04-maxina-90-day-journey-curriculum-v2.md');
  out.push('-- Do not hand-edit; re-run the generator. Idempotent: re-seed refreshes only');
  out.push('-- structural fields, never authored content. T001-T010 are authored below.');
  out.push('-- =============================================================================');
  out.push('');
  out.push('BEGIN;');
  out.push('');
  out.push(
    'INSERT INTO journey_checklist_topics',
  );
  out.push(
    '  (topic_id, curriculum_version, session, position, chapter_id, display_label, short_description, business_gate, status)',
  );
  out.push('VALUES');
  const values = [];
  for (const r of rows) {
    r.cards.forEach((c, i) => {
      const position = i + 1;
      values.push(
        `  (${lit(c.topicId)}, 'v2', ${r.session}, ${position}, ${lit(chapterFor(r.session))}, ` +
          `${lit(c.label)}, ${lit(r.purpose)}, ${lit(businessGateFor(r.session))}, 'draft')`,
      );
    });
  }
  out.push(values.join(',\n'));
  out.push('ON CONFLICT (topic_id) DO UPDATE SET');
  out.push('  session           = EXCLUDED.session,');
  out.push('  position          = EXCLUDED.position,');
  out.push('  chapter_id        = EXCLUDED.chapter_id,');
  out.push('  display_label     = EXCLUDED.display_label,');
  out.push('  short_description = EXCLUDED.short_description,');
  out.push('  business_gate     = EXCLUDED.business_gate,');
  out.push('  updated_at        = now();');
  out.push('');
  out.push("INSERT INTO journey_checklist_audit (action, detail) VALUES ('seed', 'Seeded 250 draft topics from curriculum v2');");
  out.push('');
  out.push('-- ---------------------------------------------------------------------------');
  out.push('-- Authored proof set: sessions 1-5 (T001-T010) get full content so publish');
  out.push('-- validation + My Journey have real material. Remaining topics are admin-');
  out.push('-- authored via the Checklist editor (P3).');
  out.push('-- ---------------------------------------------------------------------------');
  out.push(AUTHORED_SQL);
  out.push('');
  out.push('COMMIT;');
  out.push('');
  console.log(out.join('\n'));
}

